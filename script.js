function changeLanguage(language) {
    var elements = document.querySelectorAll('[data-translate]');
    elements.forEach(function (element) {
        var key = element.getAttribute('data-translate');
        element.textContent = lang[language][key];
    });

    // Actualiza las imágenes de las banderas
    var flagES = document.querySelector('.flag-es');
    var flagEN = document.querySelector('.flag-en');
    flagES.style.fontWeight = (language === 'es') ? 'bold' : 'normal';
    flagEN.style.fontWeight = (language === 'en') ? 'bold' : 'normal';

    // Mantiene activa la opción de idioma seleccionada
    document.getElementById(language).classList.add('active');
}







